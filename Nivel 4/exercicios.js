// 16. Soma dos números
let numeros = [10, 20, 30, 40, 50];
let soma = 0;
for (let i = 0; i < numeros.length; i++) {
    soma += numeros[i];
}
console.log("A soma é: " + soma);
//17. Média de notas
let notas = [7.0, 8.5, 6.0, 9.5];
let somaNotas = 0;
for (let i = 0; i < notas.length; i++) {
    somaNotas += notas[i];
}
let media = somaNotas / notas.length;
console.log("A média é: " + media);
